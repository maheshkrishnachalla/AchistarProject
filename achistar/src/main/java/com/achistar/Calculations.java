package com.achistar;

public class Calculations {

	/**
	 *  add method take two inputs and return sum of two inputs
	 * @param a
	 * @param b
	 * @return
 	 */
	public double add(double a, double b) {
		return a+b;
	}
	
	/**
	 * mul method take two inputs and return product of two inputs
	 * @param a
	 * @param b
	 * @return
	 */
	public double mul(double a, double b) {
		return a*b;
	}
	
	/**
	 * sub method take two inputs and return subtraction of two inputs
	 * @param a
	 * @param b
	 * @return
	 */
	public double sub(double a, double b) {
		return a-b;
	}
	
	/**
	 * div method take two inputs and return division of two inputs
	 * @param a
	 * @param b
	 * @return
	 */
	public double div(double a, double b) {
		return a/b;
	}
}
